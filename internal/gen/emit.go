var handlersTmpl = template.Must(template.New("handlers").Funcs(tmplFuncs).Parse(`{{.Header}}
package tools

import (
	"context"
	"fmt"
	"net/http"
	"net/url"
	"strings"

	"github.com/mark3labs/mcp-go/mcp"
	"github.com/mark3labs/mcp-go/server"

	signozclient "github.com/SigNoz/signoz-mcp-server/internal/client"
	gentypes "{{.TypesImportPath}}"
)

// Reference every import unconditionally so tags without (say) path or
// query params still compile. These blanks are free at runtime.
var (
	_ = context.Background
	_ = fmt.Sprintf
	_ = http.MethodGet
	_ url.Values
	_ = strings.ReplaceAll
	_ = signozclient.DefaultQueryTimeout
	_ = mcp.NewTool
	_ server.MCPServer
)

// register{{.RegisterSuffix}}Generated registers all generated tools for the
// "{{.Tag}}" OpenAPI tag.
func (h *Handler) register{{.RegisterSuffix}}Generated(s *server.MCPServer) {
{{range .Ops}}	h.genRegister{{.OperationID}}(s)
{{end}}}

{{range .Ops}}
func (h *Handler) genRegister{{.OperationID}}(s *server.MCPServer) {
	tool := mcp.NewToolWithRawSchema(
		{{goEscape .ToolName}},
		{{goEscape (firstNonEmpty .Description (firstNonEmpty .Summary (printf "%s %s" .Method .Path)))}},
		gentypes.Schema{{.OperationID}},
	)
{{if .ReadOnly}}	readOnly := true
	tool.Annotations.ReadOnlyHint = &readOnly
{{end}}{{if .Destructive}}	destructive := true
	tool.Annotations.DestructiveHint = &destructive
{{end}}{{if .HasResponse}}	tool.RawOutputSchema = gentypes.OutputSchema{{.OperationID}}
{{end}}	s.AddTool(tool, mcp.NewTypedToolHandler(h.{{.HandlerFunc}}))
}

func (h *Handler) {{.HandlerFunc}}(ctx context.Context, req mcp.CallToolRequest, in gentypes.{{.InputType}}) (*mcp.CallToolResult, error) {
	c, err := h.GetClient(ctx)
	if err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	}

	path := {{goEscape .Path}}
{{range .PathParams}}	if in.{{.GoName}} == {{zeroValue .GoType}} {
		return mcp.NewToolResultError({{goEscape (printf "missing required path parameter %q" .Name)}}), nil
	}
	path = strings.ReplaceAll(path, {{goEscape (printf "{%s}" .Name)}}, url.PathEscape({{goToString "in" .GoName .GoType}}))
{{end}}
{{if .QueryParams}}	q := url.Values{}
{{range .QueryParams}}{{if eq .GoType "string"}}	if in.{{.GoName}} != "" {
		q.Set({{goEscape .Name}}, in.{{.GoName}})
	}
{{else}}	if in.{{.GoName}} != {{zeroValue .GoType}} {
		q.Set({{goEscape .Name}}, {{goToString "in" .GoName .GoType}})
	}
{{end}}{{end}}	if len(q) > 0 {
		path += "?" + q.Encode()
	}
{{end}}
	res, err := c.Do(ctx, {{methodConst .Method}}, path, {{if .HasBody}}in.Body{{else}}nil{{end}}, signozclient.DefaultQueryTimeout)
	if err != nil {
		return mcp.NewToolResultError(err.Error()), nil
	}
	return mcp.NewToolResultText(string(res)), nil
}
{{end}}
`))

func writeHandlersFile(dir, tag string, ops []Operation, typesImportPath string) error {
	var buf bytes.Buffer
	err := handlersTmpl.Execute(&buf, map[string]any{
		"Header":          fileHeader,
		"Tag":             tag,
		"RegisterSuffix":  goIdent(tag),
		"Ops":             ops,
		"TypesImportPath": typesImportPath,
	})
	if err != nil {
		return err
	}
	src, err := format.Source(buf.Bytes())
	if err != nil {
		_ = os.WriteFile(filepath.Join(dir, "zz_generated_"+tagFileSuffix(tag)+".go.broken"), buf.Bytes(), 0o644)
		return fmt.Errorf("format handlers for tag %q: %w", tag, err)
	}
	return os.WriteFile(filepath.Join(dir, "zz_generated_"+tagFileSuffix(tag)+".go"), src, 0o644)
}

