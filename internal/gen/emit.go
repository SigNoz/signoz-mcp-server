// Emit writes generated Go source files:
//   - outHandlersDir: one zz_generated_<tag>.go per tag plus
//     zz_generated_register.go. (package tools)
//   - outRootDir: one zz_generated_<tag>.go per tag carrying typed *Input
//     structs and Schema vars composed at init via ComposeSchema. (package
//     gentools)
//
// JSON files (component schemas + per-tool schemas) are emitted separately
// by EmitComponentFiles + EmitToolFiles into <outRootDir>/components/ and
// <outRootDir>/tools/.
func Emit(ops []Operation, outHandlersDir, outRootDir, gentoolsImportPath string) error {
	if err := os.MkdirAll(outHandlersDir, 0o755); err != nil {
		return err
	}
	if err := os.MkdirAll(outRootDir, 0o755); err != nil {
		return err
	}

	byTag := groupByTag(ops)
	tags := make([]string, 0, len(byTag))
	for t := range byTag {
		tags = append(tags, t)
	}
	sort.Strings(tags)

	for _, tag := range tags {
		if err := writeHandlersFile(outHandlersDir, tag, byTag[tag], gentoolsImportPath); err != nil {
			return fmt.Errorf("emit handlers for tag %q: %w", tag, err)
		}
		if err := writeTypesFile(outRootDir, tag, byTag[tag]); err != nil {
			return fmt.Errorf("emit types for tag %q: %w", tag, err)
		}
	}
	return writeRegisterFile(outHandlersDir, tags)
}

func groupByTag(ops []Operation) map[string][]Operation {
	m := make(map[string][]Operation)
	for _, o := range ops {
		m[o.Tag] = append(m[o.Tag], o)
	}
	return m
}

func tagFileSuffix(tag string) string {
	s := strings.ToLower(tag)
	var b strings.Builder
	for _, r := range s {
		switch {
		case r >= 'a' && r <= 'z', r >= '0' && r <= '9':
			b.WriteRune(r)
		default:
			b.WriteRune('_')
		}
	}
	return b.String()
}

const fileHeader = `// Code generated by cmd/gen-tools. DO NOT EDIT.
// Source: docs/api/openapi.yml
`

// httpMethodConst maps "GET" -> "http.MethodGet".
func httpMethodConst(m string) string {
	switch strings.ToUpper(m) {
	case "GET":
		return "http.MethodGet"
	case "POST":
		return "http.MethodPost"
	case "PUT":
		return "http.MethodPut"
	case "DELETE":
		return "http.MethodDelete"
	case "PATCH":
		return "http.MethodPatch"
	case "HEAD":
		return "http.MethodHead"
	case "OPTIONS":
		return "http.MethodOptions"
	default:
		// Fall through to a quoted literal; doRequest accepts arbitrary methods.
		return fmt.Sprintf("%q", m)
	}
}

var tmplFuncs = template.FuncMap{
	"goEscape": func(s string) string { return fmt.Sprintf("%q", s) },
	"firstNonEmpty": func(a, b string) string {
		if a != "" {
			return a
		}
		return b
	},
	"zeroValue": func(t string) string {
		switch t {
		case "string":
			return `""`
		case "bool":
			return "false"
		default:
			return "0"
		}
	},
	"goToString": func(receiver, field, t string) string {
		switch t {
		case "string":
			return receiver + "." + field
		case "bool":
			return fmt.Sprintf(`fmt.Sprintf("%%t", %s.%s)`, receiver, field)
		case "int64":
			return fmt.Sprintf(`fmt.Sprintf("%%d", %s.%s)`, receiver, field)
		case "float64":
			return fmt.Sprintf(`fmt.Sprintf("%%v", %s.%s)`, receiver, field)
		default:
			return receiver + "." + field
		}
	},
	"methodConst": httpMethodConst,
	"ifOmit": func(required bool) string {
		if required {
			return ""
		}
		return ",omitempty"
	},
	"goIdentDef": func(name string) string { return goIdent(name) },
}

