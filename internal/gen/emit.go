var registerTmpl = template.Must(template.New("register").Parse(`{{.Header}}
package tools

import "github.com/mark3labs/mcp-go/server"

// RegisterGeneratedHandlers registers every MCP tool emitted by the OpenAPI
// code generator. It is wired from internal/mcp-server/server.go after the
// curated handlers so curated tools always take precedence on name
// collisions (the generator also skips such collisions at emit time).
func (h *Handler) RegisterGeneratedHandlers(s *server.MCPServer) {
{{range .Tags}}	h.register{{.}}Generated(s)
{{end}}}
`))

func writeRegisterFile(dir string, tags []string) error {
	identTags := make([]string, 0, len(tags))
	for _, t := range tags {
		identTags = append(identTags, goIdent(t))
	}
	var buf bytes.Buffer
	if err := registerTmpl.Execute(&buf, map[string]any{
		"Header": fileHeader,
		"Tags":   identTags,
	}); err != nil {
		return err
	}
	src, err := format.Source(buf.Bytes())
	if err != nil {
		return fmt.Errorf("format register: %w", err)
	}
	return os.WriteFile(filepath.Join(dir, "zz_generated_register.go"), src, 0o644)
}
