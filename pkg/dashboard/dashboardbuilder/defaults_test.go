func TestApplyDefaults_AutoLayoutGenerated(t *testing.T) {
	d := &DashboardData{
		Variables: map[string]*DashboardVariable{},
		Widgets: []WidgetOrRow{
			{ID: "w1", PanelTypes: PanelTypeGraph, Title: "W1", Query: &Query{}},
			{ID: "w2", PanelTypes: PanelTypeGraph, Title: "W2", Query: &Query{}},
		},
	}
	ApplyDefaults(d, nil, nil)

	if len(d.Layout) != 2 {
		t.Fatalf("expected 2 layout items, got %d", len(d.Layout))
	}
	if d.Layout[0].I != "w1" || d.Layout[1].I != "w2" {
		t.Errorf("layout IDs mismatch: %v", d.Layout)
	}
}

