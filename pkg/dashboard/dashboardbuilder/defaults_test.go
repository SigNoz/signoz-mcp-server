func TestApplyDefaults_VariableTypeDefaultsToDynamic(t *testing.T) {
	d := &DashboardData{
		Variables: map[string]*DashboardVariable{
			"svc": {Description: "service"},
		},
	}
	ApplyDefaults(d, nil, nil)

	v := d.Variables["svc"]
	if v.Type != VariableTypeDynamic {
		t.Errorf("expected type %q, got %q", VariableTypeDynamic, v.Type)
	}
	if v.Sort != SortASC {
		t.Errorf("expected sort %q for DYNAMIC, got %q", SortASC, v.Sort)
	}
}

